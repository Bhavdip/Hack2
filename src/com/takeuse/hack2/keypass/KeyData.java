package com.takeuse.hack2.keypass;

public class KeyData {
	
	private String title;
	
	private String userName;
	
	private String password;
	
	private String stringUrl;
	
	private String notes;

	public String getTitle() {
		return title;
	}

	public KeyData setTitle(String title) {
		this.title = title;
		return this;
	}

	public String getUserName() {
		return userName;
	}

	public KeyData setUserName(String userName) {
		this.userName = userName;
		return this;
	}

	public String getPassword() {
		return password;
	}

	public KeyData setPassword(String password) {
		this.password = password;
		return this;
	}

	public String getStringUrl() {
		return stringUrl;
	}

	public KeyData setStringUrl(String stringUrl) {
		this.stringUrl = stringUrl;
		return this;
	}

	public String getNotes() {
		return notes;
	}

	public KeyData setNotes(String notes) {
		this.notes = notes;
		return this;
	}
	
	
	
}
